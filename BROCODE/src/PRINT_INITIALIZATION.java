
public class PRINT_INITIALIZATION {

	public static void main(String[] args) {
		
		int x; //declaration
		x = 123; // assignment
		int y = 123; // initialization
		
		System.out.println("Hello World!");
	
		System.out.println("--------------------------------------");
		
		System.out.println("DECLARATION AND ASSIGNMENT: " + x);
		System.out.println("INITIALIZATION" + y);
		
		System.out.println("---------------------------------------");
		
		long z = 1234567899L; 
		System.out.println("DECLARING LONG NUMBERS " + z);
		
		System.out.println("---------------------------------------");
		
		byte c = 100;
		System.out.println("LIMIT (byte) 127 " + c);
		
		float o = 3.14f;
		System.out.println("FLOAT " + o);
		
		double u = 3.14;
		System.out.println("DOUBLE " + u);
		
		boolean v = true;
		System.out.println("BOOLEAN " + v);
		
		char symbol = '@';
		System.out.println("CHAR: " + symbol);
		
		String name = "Cay";
		System.out.println("STRING HELLO: " + name);
	}
}
