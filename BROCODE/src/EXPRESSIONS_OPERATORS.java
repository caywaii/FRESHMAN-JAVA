
public class EXPRESSIONS_OPERATORS {

	public static void main(String[] args) {
		
		int x = 10;
		x = x + 1;
		System.out.println("Addition");
		System.out.println(x);
		
		System.out.println("-----------------------------------");
		
		int y = 10;
		y = y - 1;
		System.out.println("Subtraction");
		System.out.println(y);
		
		System.out.println("-----------------------------------");
		
		int z = 10;
		z = z * 2;
		System.out.println("Multiplication");
		System.out.println(z);
		
		System.out.println("-----------------------------------");
		
		int a = 10;
		a = a / 2;
		System.out.println("Division");
		System.out.println(a);
		
		System.out.println("-----------------------------------");
		
		int b = 10;
		b = b % 3; // remainder
		System.out.println("Addition");
		System.out.println(b);
		
		System.out.println("-----------------------------------");
		
		int f = 10;
		f++;
		System.out.println("++");
		System.out.println(f);
		
		System.out.println("-----------------------------------");
		
		double d = 10;
		d = d / 3;
		System.out.println("DIVISION INCLUDING THE REMAINDERS");
		System.out.println(d);
	}
}
