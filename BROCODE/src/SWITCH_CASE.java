
public class SWITCH_CASE {

	public static void main(String[] args) {

		String day = "Friday";

		switch (day) {
		case "Sunday":
			System.out.println("It is Sunday!");
			break;
		case "Monday":
			System.out.println("It is Monday!");
			break;
		case "Tuesday":
			System.out.println("It is Monday!");
			break;
		case "Wednesday":
			System.out.println("It is Monday!");
			break;
		case "Thursday":
			System.out.println("It is Monday!");
			break;
		case "Friday":
			System.out.println("It is Monday!");
			break;
		case "Saturday":
			System.out.println("It is Monday!");
			break;
			default: System.out.println("That is not a Day!");
			
		}

	}
}
