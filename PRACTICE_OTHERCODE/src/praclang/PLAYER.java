package praclang;

import java.util.*;

public class PLAYER {

	public static void main(String[] args) {
		int valPlayer;
		
		Scanner input = new Scanner(System.in);
		
		System.out.println("Enter Valorant Player Number: ");
		valPlayer = input.nextInt();
		
		switch(valPlayer) {
		case 1:
			System.out.println("Brimstone");
			break;
		case 2:
			System.out.println("Sage");
			break;
		case 3:
			System.out.println("Pheonix");
			break;
		case 4:
			System.out.println("Jett");
		default:
			System.out.println("INPUT INVALID");
			
		
		
		
		
		}
		
		
		
	}
	
	
	
	
	
	
	
	
}
